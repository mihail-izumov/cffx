// –°–±—Ä–æ—Å –ø–æ–¥—Å–∫–∞–∑–æ–∫ –∫ –Ω–∞—á–∞–ª—å–Ω—ã–º –≤–∞—Ä–∏–∞–Ω—Ç–∞–º
function resetSuggestions(suggestionType) {
