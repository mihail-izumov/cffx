const currentQuestion3 = ref(phrases.solutions[0]);

let rotationInterval = null;
let currentQuestionIndex1 = 0;
let currentQuestionIndex2 = 0;
let currentQuestionIndex3 = 0;

// === Функции ===
function isInitialSuggestions(type) {
  const current = currentSuggestions[type];
  const initial = suggestions[selectedCategory.value][type].initial;
  return JSON.stringify(current) === JSON.stringify(initial);
}

function resetSuggestions(type) {
  currentSuggestions[type] = [...suggestions[selectedCategory.value][type].initial];
  branchCounters[type] = 0;
}

function selectSuggestion(field, suggestion, type) {
  const text = form[field].trim();
  const isNewBranch = isInitialSuggestions(type);

  if (text) {
    form[field] = isNewBranch
      ? `${text}. ${suggestion.charAt(0).toUpperCase()}${suggestion.slice(1)}`
      : `${text} ${suggestion}`;
  } else {
    form[field] = suggestion.charAt(0).toUpperCase() + suggestion.slice(1);
  }

  branchCounters[type] = (branchCounters[type] || 0) + 1;
  selectedSuggestions[type].push(suggestion);
  updateSuggestions(type, suggestion);
}

function updateSuggestions(type, word) {
  const next = suggestions[selectedCategory.value][type][word];
  currentSuggestions[type] = next && next.length > 0 ? [...next] : [...suggestions[selectedCategory.value][type].initial];
}

function startRotation(num) {
  clearInterval(rotationInterval);
  const phrasesArr = num === 1 ? phrases.emotions : num === 2 ? phrases.facts : phrases.solutions;
  let index = num === 1 ? currentQuestionIndex1 : num === 2 ? currentQuestionIndex2 : currentQuestionIndex3;

  rotationInterval = setInterval(() => {
    index = (index + 1) % phrasesArr.length;
    if (num === 1) {
      currentQuestionIndex1 = index;
      currentQuestion1.value = phrasesArr[index];
    } else if (num === 2) {
      currentQuestionIndex2 = index;
      currentQuestion2.value = phrasesArr[index];
    } else {
      currentQuestionIndex3 = index;
      currentQuestion3.value = phrasesArr[index];
    }
  }, 3000);
}

// === Сброс при смене категории ===
watch(selectedCategory, () => {
  // Сброс формы
  form.emotionalRelease = '';
  form.factualAnalysis = '';
  form.constructiveSuggestions = '';

  // Сброс подсказок
  Object.keys(currentSuggestions).forEach(type => {
    currentSuggestions[type] = [...suggestions[selectedCategory.value][type].initial];
    selectedSuggestions[type] = [];
    branchCounters[type] = 0;
  });

  // Сброс вопросов
  currentQuestion1.value = phrases.emotions[0];
  currentQuestion2.value = phrases.facts[0];
  currentQuestion3.value = phrases.solutions[0];
  currentQuestionIndex1 = currentQuestionIndex2 = currentQuestionIndex3 = 0;
});

onUnmounted(() => clearInterval(rotationInterval));
</script>

